package products;

public abstract class Motorbike {

    public String name;
    public String color;
    public String displacement;
    public String value;

    public String getName() {
        return name;
    }
    public String getColor() {
        return color;
    }
    public String getDisplacement() {
        return displacement;
    }
    public String getValue() {
        return value;
    }
    
    
}
